void keyboard_post_init_user(void) {
    rgb_matrix_enable();
    rgb_matrix_mode(RGB_MATRIX_RIVERFLOW);
    rgb_matrix_sethsv(212,150,201);
}

layer_state_t layer_state_set_user(layer_state_t state) {
    set_layer_rgb(get_highest_layer(state));
    return state;
}

