// HELPERS

void set_layer_rgb(uint32_t layer) {
    switch (layer) {
        case _BASE: {
            // fall through
        }
        case _FN0: {
            rgb_matrix_mode(RGB_MATRIX_RIVERFLOW);
            break;
        } 
        case _BABY: {
            rgb_matrix_mode(RGB_MATRIX_MULTISPLASH);
            break;
        }
        default: {
            break;
        }
    }
}